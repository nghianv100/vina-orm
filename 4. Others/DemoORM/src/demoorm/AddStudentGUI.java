    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            MySQLConnection.Builder builder = new MySQLConnection.Builder();
            MySQLConnection conn = builder.setHost(host).setPort(port).setDbName(dbname).setUsername(username).setPassword(password).build();
            conn.open();
            Student temp = new Student();
            temp.setStudentId(txtId.getText());
            temp.setStudentClass(txtClass.getText());
            temp.setStudentGPA(Float.valueOf(txtGPA.getText()));
            temp.setStudentName(txtName.getText());
            conn.insert(temp);
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            this.dispose();
        }
    }//GEN-LAST:event_btnAddActionPerformed

